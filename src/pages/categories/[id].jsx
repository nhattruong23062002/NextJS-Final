export async function getStaticProps(req) {
  try {
    const { params } = req;
    const response = await axiosClient.get(`/categories/${params.id}`);

    return {
      props: {
        category: response.data.payload,
      },

      revalidate: 60 * 60 * 24 * 30,
    };
  } catch (error) {
    return {
      notFound: true,
    };
  }
}

